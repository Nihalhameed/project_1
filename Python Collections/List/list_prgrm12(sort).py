#Sorting a list :
#Ascending order
#Descending order
lst=[10,12,1,4,55,15,20,25,30,35,40]

lst.sort() #Sort() function prints the list in ascending order
print(lst)

# To sort the list in descending order we use reverse=True in sort() function,i.e sort(reverse=True)

#lst=[10,12,1,4,55,15,20,25,30,35,40]

lst.sort(reverse=True) #Prints the list in descending order
print(lst)
