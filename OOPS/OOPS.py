#OOPS(Object-Oriented programming) :
# 3 concepts :

#1. Class :
#          Blueprint or Base Model

#2. Object :
#          Whatever designed with the base model(class) is called >> Object
#3. Reference :
#          Operations performed upon the object is called >> Reference


#Real time example :

 #Television : class


#Brands ==> samsung,philips,sony.....etc : Object

#Reference : operations perfromed  like :power onn/off,volume up/down,channel change etc


#PHONE : Class
#BRANDS - APPLE,SAMSUNG...ETC : Object
#texting,calling etc : Reference


