#Compound Assignment Operator
#Addition +
num1=10
num1+=1 #num1=num1+1
print(num1)

#Subtraction -
num2=10
num2-=6 #num2=num2-6
print(num2)

#Multiplication *
num3=10
num3*=5
print(num3)

#Division /
num4=10
num4/=2
print(num4)

#Floor division - discards the decimal part
num5=10
num5//=3
print(num5)

#Modulus % (remainder)
num6=10
num6%=3
print(num6)

#Exponential or Power **
num7=10
num7**=2
print(num7)