#Syntax :

# lst=[print... range] (print denotes-what to print & then range )

#Method 1 of list comprehension :

#Example :
#1. Print elements from 1 to 100 in a list:

lst=[i for i in range(1,101)]
print(lst)

#2. Print elements from 10 to 15 in a list:

lst=[i for i in range(10,51)]
print(lst)

#3. Print range of square of elements from 1 to 20 :
lst=[i**2 for i in range(1,21)]
print(lst)





